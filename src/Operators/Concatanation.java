package Operators;

public class Concatanation {
    public static void main(String[] args) {
        String first_name = "Pramod";
        String last_name = "Dutta";
        // Concatenation Operator  +
        // + -> int + int -> math
        // + -> String -> combine -> concat
        System.out.println(first_name + last_name);
    }
}
