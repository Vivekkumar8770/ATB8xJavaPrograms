package If_Else;

public class Vowels_Cons {
    public static void main(String[] args) {
        // char c = 'a' -> Vowel or consonant
        // vowel ->  a, e, i o, u
        char ch = 'p';
        if ( ch == 'a' || ch == 'e' || ch =='o' || ch == 'i' || ch == 'u'){
            System.out.println("Vowel");
        }else{
            System.out.println("Consonant");
        }



    }
}
